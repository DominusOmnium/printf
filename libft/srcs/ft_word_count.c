#include "libft.h"

int	ft_word_count(char const *s, char c)
{
	int	i;
	int count;

	i = 0;
	count = 0;
	while (*(s + i) != 0)
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == 0))
			count++;
		i++;
	}
	return (count);
}
